// rules for naming variables

// you cannot start with number 
// example :-
// 1value (invalid)
// value1 (valid)

var value = 2;
console.log(value);

// you can use only underscore _ or dollar symbol
// first_name (valid)
// _firstname (valid)
var _firstname = 'rahul kumar';
console.log(_firstname)
// first$name (valid)
// $firstname (valid)

// you cannot use spaces
var first_name ='rahul'; // snake case writing
var firstName ='harshit'; // camel case writing
// first name (invalid )

